# Mathematical operator in python

# Addition
print("Mathematical operator + (addition) ")
print(5 + 2)

# substraction
print("Mathematical operator - (substraction) ")
print(5 - 2)

# multiplication
print("Mathematical operator * (multiplication) ")
print(5 * 2)

# Division
print("Mathematical operator / (division) This will return quotient as it is ")
print(5/2)

print("Mathematical operator // (floor division) This will also return quotient, however ony the integer part, "
      "the fractional part is eliminated  ")
print(5 // 2)

print("Mathematical operator % (modulus) This returns remainder from division ")
print(5 % 2)
